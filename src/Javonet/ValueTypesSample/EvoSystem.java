public class EvoSystem {
  protected NObject javonetHandle;

  public EvoSystem() {
    try {
      javonetHandle = Javonet.New("Javonet.ValueTypesSample.EvoSystem");
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
    }
  }

  public EvoSystem(NObject handle) {
    this.javonetHandle = handle;
  }

  public void setJavonetHandle(NObject handle) {
    this.javonetHandle = handle;
  }
  /** Method */

  public Object GetScriptVariable(java.lang.Integer ScriptID, java.lang.String VariableName) {
    try {
      Object res = javonetHandle.invoke("GetScriptVariable", ScriptID, VariableName);
      if (res == null) return null;
      return ConvertToConcreteInterfaceImplementation(res);
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return null;
    }
  }

  static {
    try {
      Activation.initializeJavonet();
    } catch (java.lang.Exception e) {
      e.printStackTrace();
    }
  }
}
